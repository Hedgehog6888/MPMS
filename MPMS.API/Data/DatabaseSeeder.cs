    /// <summary>
    /// Seeds 4 test users (one per role) if no users exist yet.
    /// Safe to call on every startup — does nothing if data is already present.
    /// </summary>
    public static async Task SeedAsync(IServiceProvider services)
    {
        using var scope = services.CreateScope();
        var db = scope.ServiceProvider.GetRequiredService<ApplicationDbContext>();

        // Apply pending migrations automatically
        await db.Database.MigrateAsync();

        await SeedServicesAsync(db);

        if (!await db.Users.AnyAsync())
        {
            var roles = await db.Roles.ToDictionaryAsync(r => r.Name);
            var users = new[]
            {
                CreateSeedUser("Иван", "Администратов", "admin", "admin@mpms.local", "admin123", roles["Administrator"].Id),
                CreateSeedUser("Алексей", "Менеджеров", "manager", "manager@mpms.local", "manager123", roles["Project Manager"].Id),
                CreateSeedUser("Сергей", "Прорабов", "foreman", "foreman@mpms.local", "foreman123", roles["Foreman"].Id),
                CreateSeedUser("Пётр", "Работников", "worker", "worker@mpms.local", "worker123", roles["Worker"].Id)
            };
            db.Users.AddRange(users);
            await db.SaveChangesAsync();
            return;
        }

    }

    private static async Task SeedServicesAsync(ApplicationDbContext db)
    {
        var categoryNames = DefaultServiceCategories;
        var categories = await db.ServiceCategories.ToListAsync();
        if (categories.Count == 0)
        {
            db.ServiceCategories.AddRange(categoryNames.Select((x, idx) => new ServiceCategory
            {
                Id = Guid.NewGuid(),
                Name = x,
                Description = $"Категория работ: {x}",
                SortOrder = idx + 1,
                IsActive = true
            }));
            await db.SaveChangesAsync();
            categories = await db.ServiceCategories.ToListAsync();
        }
        else
        {
            foreach (var name in categoryNames)
            {
                if (categories.All(c => !string.Equals(c.Name, name, StringComparison.OrdinalIgnoreCase)))
                {
                    db.ServiceCategories.Add(new ServiceCategory
                    {
                        Id = Guid.NewGuid(),
                        Name = name,
                        Description = $"Категория работ: {name}",
                        SortOrder = Array.IndexOf(categoryNames, name) + 1,
                        IsActive = true
                    });
                }
            }
            await db.SaveChangesAsync();
            categories = await db.ServiceCategories.ToListAsync();
        }

        var catMap = categories.ToDictionary(c => c.Name, c => c.Id, StringComparer.OrdinalIgnoreCase);
        var seedItems = BuildServiceItems(catMap);
        var existing = await db.ServiceTemplates.ToDictionaryAsync(x => x.Article ?? string.Empty);
        var now = DateTime.UtcNow;

        foreach (var item in seedItems)
        {
            if (existing.TryGetValue(item.Article, out var ex))
            {
                ex.Name = item.Name;
                ex.Description = item.Description;
                ex.Unit = item.Unit;
                ex.BasePrice = item.BasePrice;
                ex.CategoryId = item.CategoryId;
                ex.IsActive = true;
                ex.UpdatedAt = now;
            }
            else
            {
                db.ServiceTemplates.Add(new ServiceTemplate
                {
                    Id = Guid.NewGuid(),
                    Name = item.Name,
                    Description = item.Description,
                    Unit = item.Unit,
                    Article = item.Article,
                    BasePrice = item.BasePrice,
                    CategoryId = item.CategoryId,
                    IsActive = true,
                    CreatedAt = now,
                    UpdatedAt = now
                });
            }
        }

        await db.SaveChangesAsync();
    }

